#Assignment Operations

# Assigning a value to a variable
x = 10

# Adding and assigning a value
x += 5  # x is now 15

# Subtracting and assigning a value
x -= 3  # x is now 12

# Multiplying and assigning a value
x *= 2  # x is now 24

# Dividing and assigning a value
x /= 4  # x is now 6.0

# Modulus and assigning a value
x %= 5  # x is now 1.0

# Exponentiation and assigning a value
x **= 3  # x is now 1.0

# Floor division and assigning a value
x //= 2  # x is now 0.0