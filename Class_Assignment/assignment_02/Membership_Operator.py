#Membership Operator
#in, not in
#in checks if the value is present in the sequence
#not in checks if the value is not present in the sequence
#Example
a = 10
b = 20
list = [10, 20, 30, 40, 50]
print("a in list is", a in list)
print("b in list is", b in list)
print("a not in list is", a not in list)