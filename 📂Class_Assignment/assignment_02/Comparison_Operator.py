#Comparion Operator
# ==, !=, >, <, >=, <=

a = 10
b = 10
#== checked the equality of two operands
print("a == b is", a == b)
#!= checked the inequality of two operands
print("a != b is", a != b)
#> checked the greater than of two operands
print("a > b is", a > b)
#< checked the less than of two operands
print("a < b is", a < b)
#>= checked the greater than or equal to of two operands
print("a >= b is", a >= b)
#<= checked the less than or equal to of two operands
print("a <= b is", a <= b)
